package com.hudie.Operator;

public class OperatorDemo4 {
    public static void main(String[] args) {
        //关系运算符的应用
        int a = 10;
        int b = 20;
        boolean rs = a > b;
        System.out.println(rs);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(10 == 10);
        System.out.println(a != b);

    }
}
