package com.hudie.variable;

public class VariableDemo {
    public static void main(String[] args) {
    // 目标：认识变量
        int age = 20;
        System.out.println(age);

    // 记住一个人的成绩
        double score = 87.2;
        System.out.println(score);

        System.out.println("---------------------------------------");

    //变量的特点
        int age2 = 11;
        System.out.println(age2);

        age2 = 23;
        System.out.println(age2);

        age2 = age2 + 3;
        System.out.println(age2);

        System.out.println("-----------------------------");


//        需求：钱包有9.5元，收到了20元，又发出去10元，请输出各阶段钱包的情况。
        double money = 9.5;
        System.out.println(money);

        money = money + 20;
        System.out.println(money);

        money = money - 10;
        System.out.println(money);

        System.out.println("-------------------------------");





    }
}
